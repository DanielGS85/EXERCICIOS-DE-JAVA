public class ContagemRegressiva { // define a classe chamada ContagemRegressiva
    public static void main(String[] args) { // método principal, ponto de entrada do programa
        int num = 10; // declara a variável 'num' e inicializa com o valor 10

        while (num >= 1) { // enquanto 'num' for maior ou igual a 1, executa o bloco abaixo
            System.out.println(num); // exibe o valor atual de 'num' no console
            num--; // decrementa 'num' em 1 (num = num - 1)
        }
    } // fim do método main
} // fim da classe ContagemRegressiva
