public class VariavelEMaior { // define a classe chamada VariavelEMaior
    public static void main(String[] args) { // método principal, ponto de entrada do programa
        int num1 = 1; // declara a variável num1 e atribui o valor 1
        int num2 = 3; // declara a variável num2 e atribui o valor 3

        if (num1 > num2) { // verifica se num1 é maior que num2
            System.out.println("O numero " + num1 + " é maior que " + num2); // imprime que num1 é maior
        } else { // caso contrário, ou seja, se num1 não for maior que num2
            System.out.println("O numero " + num2 + " é maior que " + num1); // imprime que num2 é maior
        }
    } // fim do método main
} // fim da classe VariavelEMaior
